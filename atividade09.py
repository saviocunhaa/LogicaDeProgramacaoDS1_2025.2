# Atividade 09 – Cálculo de Área
# Peça ao usuário a largura e a altura de um retângulo.
# Calcule e mostre a área total.


# Entrada de dados
largura = float(input("Digite a largura do retângulo: "))
altura = float(input("Digite a altura do retângulo: "))

# Cálculo da área
area = largura * altura

# Exibição do resultado
print(f"A área total do retângulo é: {area} m²")
